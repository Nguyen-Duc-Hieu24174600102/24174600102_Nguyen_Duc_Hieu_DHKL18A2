print("1. Phim tình cảm\n2. Phim kinh dị\n3. Phim hoạt hình\n4. Phim khoa học viễn tưởng")
chon = int(input("Chọn thể loại phim (1-4): "))

if chon == 1:
    print("Phim tình cảm")
elif chon == 2:
    print("Phim kinh dị")
elif chon == 3:
    print("Phim hoạt hình")
elif chon == 4:
    print("Phim khoa học viễn tưởng")
else:
    print("Lựa chọn không hợp lệ")